package Calculator;

public class Calculator {
    public int sumWithReturn(int a, int b) {
        return a + b;
    }
    public int minusWithReturn(int c, int d) {
        return c - d;
    }
    public int multiplyWithReturn(int e, int f) {
        return e * f;
    }
    public int divideWithReturn(int g, int h) {
        return g / h;
    }
}
